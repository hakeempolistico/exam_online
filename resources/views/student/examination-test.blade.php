    <script>

        $('.btn.next').on('click', function(){

            if($("div.active").next('.row-question') && $("div.active").next('.row-question').length != 0){
                var active = $("div.active").removeClass('active').addClass('inactive')
                active .next('.row-question').addClass('active').removeClass('inactive')
                if($("div.active").next('.row-question').length == 0){
                    $('.btn.next').addClass('hidden')
                    $('.btn.finish').removeClass('hidden')
                }
            }
        })

        $('.btn.back').on('click', function(){

            if($("div.active").prev('.row-question') && $("div.active").prev('.row-question').length != 0){

                $('.btn.next').removeClass('hidden')
                $('.btn.finish').removeClass('hidden').addClass('hidden')

                var active = $("div.active").removeClass('active').addClass('inactive')
                active .prev('.row-question').addClass('active').removeClass('inactive')
            } 
        })
    </script>



    
    




